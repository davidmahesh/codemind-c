#include<stdio.h>
int main()
{
    char alp,V,I,B,G,Y,O,R;
    scanf("%c",&alp);
    if(alp=='V')
    {
        printf("Violet");
    }
    else if(alp=='I')
    {
        printf("Indigo");
    }
    else if(alp=='B')
    {
        printf("Blue");
    }
    else if(alp=='G')
    {
        printf("Green");
    }
    else if(alp=='Y')
    {
        printf("Yellow");
    }
    else if(alp=='O')
    {
        printf("Orange");
    }
    else if(alp=='R')
    {
        printf("Red");
    }
    else
    {
        printf("-1");
    }
    
    
    
    
}